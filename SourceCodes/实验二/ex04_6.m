k=input('请输入时间段k(1为起始值)：')
X=[1000;900;800;700;600];
L=[0 1 0.98 0 0;0.98 0 0 0 0;0 0.96 0 0 0;0 0 0.93 0 0;0 0 0 0.90 0];
x=zeros(5,k);
x(:,1)=[1000;900;800;700;600];
for i=1:1:(k-1)
    X=L*X;
    x(:,i+1)=X;
end
    
subplot(1,2,1),bar(x','group'),title('1到k时间段的人口变化（每个时间段间隔15年）')
subplot(1,2,2),bar(x,'group'),title('1到5年龄组的人口变化')
N=0;
for i=1:5
    N=N+x(i,k);
end
A1=x(1,k)/N
A2=x(2,k)/N
A3=x(3,k)/N
A4=x(4,k)/N
A5=x(5,k)/N